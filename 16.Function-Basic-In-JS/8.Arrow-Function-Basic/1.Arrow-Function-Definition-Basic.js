/*
    Arrow Function Concept
    ======================
    Arrow Function in JS are ES6 update also called "Fat Arrow".
    It is used to shorten the function definition of function 
    expression.

    Syntax:
        () =>{

        };
*/

// Declaring 'use strict' statement
'use strict';

// Defining Arrow Function
let textPrinting = () =>{
    console.log(`Arrow Function Concept`);
};

// Calling The Function
console.log(textPrinting); // results the arrow function definition as string format
textPrinting(); // results the arrow function work 
