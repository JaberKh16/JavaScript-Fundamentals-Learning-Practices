/*
'typeof' Operator is used to find out the type of variables.
General Syntax:  
        typeof  var_name;
*/

// Declaring 'use strict' statement
'use strict';

// Declaring some variable
let numeric_value = 4; // a numeric type variable
let string_value = "some"; // a string type variable
let boolean_value = true; // a boolean type variable

console.log(typeof numeric_value); // return the type as 'numeric'
console.log(typeof string_value); // return the type as 'string'
console.log(typeof boolean_value); // return the type as 'boolean'