/*
    Array Property : array.length
    =============================
    array.length is used to get the length of an array which return the length
    as numeric type value.
*/

// Declaring 'use strict' statement
'use strict';

// Declaring an array 
let arr = [1, 2, 4, 6, 8, 12, 0];
console.log(arr);

// Getting The Length Of The Array
let arrayLength = arr.length; // gets the array length as numeric value
console.log(arrayLength);