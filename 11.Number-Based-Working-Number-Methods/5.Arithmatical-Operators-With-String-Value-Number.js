// Declaring 'use strict' statement
'use strict';

// Checking Arithmatical Operation With String Numeric Value Number
console.log(`2 + "3" : ${2 + "3"}`); // results concatenation of 23 so '23'
console.log(`2 - "1" : ${2  - "1"}`); // results subtraction operation and value is 1
console.log(`12 * "2" : ${12 * "2"}`); // results multiplication operation and value is 24
console.log(`12 / "2" : ${12 / "2"}`); // results division operation and value is 6
console.log(`16 % "4" : ${16 % "4"}`); // results modulus operation and value is 4
console.log(`"16" % "4" : ${"16" % "4"}`);
console.log(`2 ** "3" ${2 ** "3"}`); // results exponential operation and value is 8