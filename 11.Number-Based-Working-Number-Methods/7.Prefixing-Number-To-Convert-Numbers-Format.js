// Prefixing Integer Type Number with 'x'
let number_1 = 0x7223; // though prefix with 'x' converted to hexadecimal
console.log(`Hexadecimal Prefixed Value: ${number_1}`); // result hexadecimal value of this variable

let number_2 = 0323; // though prefix with '0' converted to octal
console.log(`Octal Prefixed Value: ${number_2}`);

let number_3 = 323.0; // decimal prefix
console.log(`Decimal Prefixed Value: ${number_3}`);
